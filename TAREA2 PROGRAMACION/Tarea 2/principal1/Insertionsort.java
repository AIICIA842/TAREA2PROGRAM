public class Insertionsort {
    public static void sort(int[] arr) {
        int numero = arr.length;
        
        for (int i = 1; i < numero; ++i) {
            int key = arr[i];
            int j = i - 1;

            while (j >= 0 && arr[j] > key) {
                arr[j + 1] = arr[j];
                j = j - 1;
            }
            arr[j + 1] = key;
        }
    }
}